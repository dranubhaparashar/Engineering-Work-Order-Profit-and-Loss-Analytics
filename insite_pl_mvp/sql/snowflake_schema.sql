    SCOPE_OF_WORK_PERFORMED VARCHAR
);

CREATE OR REPLACE TABLE FACT_BILLING (
    INVOICE_ID VARCHAR,
    WORK_ORDER_NUMBER VARCHAR,
    INVOICE_DATE DATE,
    BILLING_STATUS VARCHAR,
    BILLED_AMOUNT NUMBER(18,2)
