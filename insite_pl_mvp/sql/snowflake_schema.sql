CREATE OR REPLACE TABLE DIM_WORK_ORDER (
    WORK_ORDER_NUMBER VARCHAR PRIMARY KEY,
    CUSTOMER VARCHAR,
    ACCOUNT VARCHAR,
    BUDGETED_PROGRAM VARCHAR,
    JOB_CATEGORY VARCHAR,
    BID_REGION VARCHAR,
    INTAKE_DATE DATE,
    PLANNED_CLOSE_DATE DATE,
    ACTUAL_CLOSE_DATE DATE,
    STATUS VARCHAR,
    TARGET_MARGIN_PCT NUMBER(8,4),
    MEDIUM_RISK_THRESHOLD_PCT NUMBER(8,4),
    HIGH_RISK_THRESHOLD_PCT NUMBER(8,4)
);

