    WORK_UNIT_DESCRIPTION VARCHAR,
    UNIT_TYPE VARCHAR,
    WORK_UNIT_QTY NUMBER(18,2),
    ITEM_RATE NUMBER(18,4),
    TOTAL_RATE NUMBER(18,2)
